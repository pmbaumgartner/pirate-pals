local FALLBACK = {'111','101','101','101','111'}

local function glyphOf(ch)
  return FONT[ch] or FONT[ch:upper()] or FALLBACK
end

function M.textWidth(s, sc)
  sc = sc or 1
  s = tostring(s)
  local w = 0
  for i = 1, #s do
    w = w + (#glyphOf(s:sub(i, i))[1] + 1) * sc
  end
  return w - sc
end

function M.drawText(s, x, y, color, sc, align)
  sc = sc or 1
  s = tostring(s)
  if align == 'center' then x = x - math.floor(M.textWidth(s, sc) / 2) end
  if align == 'right' then x = x - M.textWidth(s, sc) end
  gfx.setColor(color[1], color[2], color[3], color[4] or 1)
  local cx = x
  for i = 1, #s do
    local g = glyphOf(s:sub(i, i))
    for r = 1, 5 do
      local row = g[r]
      for c = 1, #row do
        if row:sub(c, c) == '1' then
          gfx.rectangle('fill', cx + (c - 1) * sc, y + (r - 1) * sc, sc, sc)
        end
      end
    end
    cx = cx + (#g[1] + 1) * sc
  end
end

-- Text with a dark drop shadow, for busy backgrounds.
function M.drawTextO(s, x, y, color, sc, align)
  sc = sc or 1
  M.drawText(s, x + sc, y + sc, CO.ink, sc, align)
  M.drawText(s, x, y, color, sc, align)
end

